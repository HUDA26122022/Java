package latihan1;

public class OperasiPerbandingan {
    public static void main (String[] args){
        // operasi perbandingan sederhana
        int a = 100;
        int b = 100;

        //tipe-tipe operasi perbandingan
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println( a != b);

    }
    
}
