package latihan1;

public class TipeDataString {
    public static void main(String[] args) {
        //pemanggilan/penambahan value secara langsung
        String Ahmad = "ahmad ";
        String Huda = "huda";
        String Gabungkan = Ahmad +" "+ Huda; //ditambahkan "" agar bisa menambahkan sebuah spasi di bagian text kalimatnya 
        
        //penambahan value secara tidak langsung 
        String Hudaa;
        Hudaa = "Ahmad mulia huda";
    

        // pemanggilan
        System.out.print(Ahmad);
        System.out.println(Huda);
        System.out.println(Gabungkan);
        System.out.print(Hudaa);
    }
}
